public class Circle {

    double r = 8;
    double pi = 3.14;


    void tinhChuVi() {
        double chuvi = 2 * r * pi;
        System.out.println("Chu vi hinh tron la:" + chuvi);
    }

    void dienTichHT() {
        double dt = r * r * pi;
        System.out.println("Dien tich hinh tron la:" + dt);
    }





}
